#include "libmatrices.h"

t_mat4	*m_mat4cpy(t_mat4 *matrix)
{
	t_mat4	*copy;

	copy = (t_mat4 *)malloc(sizeof(t_mat4));
	*copy = *matrix;
	return (copy);
}
